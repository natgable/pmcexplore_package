#'
#' @format A data frame with 111 rows and 2 variables:
#' \describe{
#'   \item{descriptor}{the MeSH term}
#'   \item{branch}{the branch number containing a letter A through Z, followed by a two digit number}
#'   ...
#' }
"mesh_top_level"